var nombre = "ainhobara";
var apellido = "calvo";

// Primera concatenación
var nombreCompleto = nombre + " " + apellido;
console.log(nombreCompleto);

var nombreCompleto2 = "Tiziana" + " " + "Calvo";
console.log(nombreCompleto2);

// Segunda concatenación
var junto = nombre + " " + 219;
console.log(junto);

// El número 219 sigue siendo número,
// pero al concatenarlo con una cadena se lee como texto.
junto = nombre + 78 + 17;
console.log(junto);