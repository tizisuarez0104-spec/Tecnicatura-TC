# Ciclo while (Mientras o durante)
contador = 0
while contador < 100:
    print('Ejecutamos nuestro ciclo while ', contador)
    contador += 1
else:
    print('Fin del ciclo while')

# Imprimir números del 0 al 5 con el ciclo while
maximo = 5
contador = 0
while contador <= maximo:
    print(contador)
    contador += 1
